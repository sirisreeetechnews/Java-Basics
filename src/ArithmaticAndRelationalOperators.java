
public class ArithmaticAndRelationalOperators {
    public static void main(String[] args) {
        int a = 10;
        int b=5;
        String s = "Hello";

        //Arithmatic Operators
        System.out.print("Addition of a & b  -->  ");
        System.out.println(a + b); // a and b are numbers, it will perform addition
        System.out.print("Concatenation of a & s  -->  ");
        System.out.println(a + s); // a is number and s is String, it will perform concatenation
        System.out.print("subtraction of a & b  -->  ");
        System.out.println(a - b); // a and b are numbers, it will perform subtraction
        System.out.print("Multiplication of a & b  -->  ");
        System.out.println(a * b); // a and b are numbers, it will perform multiplication
        System.out.print("division of a & b  -->  ");
        System.out.println(a / b);  // a and b are numbers, it will perform division
        System.out.print("division returns reminder of a & b  -->  ");
        System.out.println(a % b); // a and b are numbers, it will perform division and return reminder


        // Logical Operators
        System.out.print("a > b  -->  ");
        System.out.println(a > b); // a and b are numbers, it will perform addition
        System.out.print("a < b  -->  ");
        System.out.println(a < b); // a is number and s is String, it will perform concatenation
        System.out.print("a >= b  -->  ");
        System.out.println(a >= b); // a and b are numbers, it will perform subtraction
        System.out.print("a <= b  -->  ");
        System.out.println(a <= b); // a and b are numbers, it will perform multiplication
        System.out.print("a == b  -->  ");
        System.out.println(a == b);  // a and b are numbers, it will perform division
        System.out.print("a != b  -->  ");
        System.out.println(a != b); // a and b are numbers, it will perform division and return reminder
    }
}
